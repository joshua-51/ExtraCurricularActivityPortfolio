package LEARNING.DataTypes;

public class primitive {
    byte    _byte_  = -128;        // stores whole number from -128 to 127
    short   _short_ =  32767;      // stores whole numbers from -32768 to 32767
    int     _int_   =  2000000000; // stores whole numbers from - 2 billion to 2 billion
    long    _long_  =  783;        // stores big numbers up to 9 quintillion
    float   _float_ =  2.23323f;   // decimals up to 7 digits
    double  _double_=  434.244245; //decimals, up to 16 digits
    boolean _bool_  = true;        // true or false
    char    _char_  = 'a';         // for one singular character
}

